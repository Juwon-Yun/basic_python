def add(a, b):
    return a+b
def minus(a, b):
    return a-b
def multi(a, b):
    return a*b
def devide(a,b):
    return a/b
def percent(a,b):
    return a%b

sum = add(3, 6)
min = minus(3, 4)
mul = multi(3, 4)
dev = devide(3, 4)
per = percent(3, 4)

print("sum:", sum)
print("min:", min)
print("mul:", mul)
print("dev:", dev)
print("per:", per)